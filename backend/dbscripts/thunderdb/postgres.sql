-- Table to store certificates associated with various entities.
CREATE TABLE CERTIFICATE (
    ID INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    SERVER_ID VARCHAR(255) NOT NULL,
    CERT_ID VARCHAR(36) NOT NULL,
    REF_TYPE VARCHAR(20) NOT NULL,
    REF_ID VARCHAR(36) NOT NULL,
    TYPE VARCHAR(20) NOT NULL,
    VALUE TEXT NOT NULL,
    CREATED_AT TIMESTAMPTZ DEFAULT NOW(),
    UPDATED_AT TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE (CERT_ID, SERVER_ID),
    UNIQUE (REF_TYPE, REF_ID, SERVER_ID)
);

-- Index for server isolation on CERTIFICATE
CREATE INDEX idx_certificate_server_id ON CERTIFICATE (SERVER_ID);
